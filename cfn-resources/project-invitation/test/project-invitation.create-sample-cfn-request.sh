set -o errexit
set -o nounset
set -o pipefail

username="testing@mongodb.com"

jq --arg ProjectId "$ATLAS_PROJECT_ID" \
	--arg username "$username" \
	'.desiredResourceState.ProjectId?|=$ProjectId' \
	"$(dirname "$0")/project-invitation.sample-cfn-request.json"
